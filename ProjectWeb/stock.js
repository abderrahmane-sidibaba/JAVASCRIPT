function updateStock1(newStock) {
    // Get the selected option
    let option = document.getElementById("ST1").value;

    let stock = document.getElementById("ST11");
    if (option == 'XS') {
        stock.value = '30';
    }
    if (option == 'S') {
        stock.value = '41';
    }
    if (option == 'M') {
        stock.value = '22';
    }
    if (option == 'L') {
        stock.value = '27';
    }
    if (option == 'XL') {
        stock.value = '20';
    }
    if (option == 'XXL') {
        stock.value = '17';
    }
}
